// Функция для проверки и пропуска пользователя в клуб
function checkEntry() {
    // Запрашиваем количество людей, которые хотят зайти в клуб
    let numberOfPeople = parseInt(prompt("Сколько людей хотят зайти в клуб?"), 10);

    // Проверяем, что количество людей корректное
    if (isNaN(numberOfPeople) || numberOfPeople <= 0) {
        alert("Введите корректное количество людей.");
        return;
    }

    // Проверяем каждого человека
    for (let i = 0; i < numberOfPeople; i++) {
        // Запрашиваем имя у пользователя
        let name = prompt("Введите ваше имя:");

        // Проверяем, что имя начинается с буквы А или а
        if (/^[Аа]/.test(name)) {
            // Запрашиваем возраст
            let age = parseInt(prompt("Введите ваш возраст:"), 10);

            // Проверяем, что возраст больше 20 и не превышает 40
            if (age > 20 && age <= 40) {
                // Запрашиваем сумму денег в кармане
                let money = parseFloat(prompt("Введите количество денег в кармане:"));

                // Проверяем, что в кармане больше 100 долларов
                if (money > 100) {
                    alert(name + ", добро пожаловать в клуб!");
                } else {
                    alert(name + ", вам нужно больше 100 долларов, чтобы войти в клуб.");
                }
            } else {
                alert(name + ", ваш возраст должен быть больше 20 и не превышать 40.");
            }
        } else {
            alert(name + ", ваше имя должно начинаться с буквы А или а.");
        }
    }
}

// Вызов функции для проверки входа
checkEntry();
